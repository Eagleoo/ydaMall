package com.mall.model;

public class PlaneOrdersBean {
	private String orderId;
	private String ordertime;
	private String fromFly;
	private String toFly;
	private String departureDate;
	private String flightNo;
	private String seat;
	private String money;
	private String orderState;
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getOrdertime() {
		return ordertime;
	}
	public void setOrdertime(String ordertime) {
		this.ordertime = ordertime;
	}
	public String getFromFly() {
		return fromFly;
	}
	public void setFromFly(String fromFly) {
		this.fromFly = fromFly;
	}
	public String getToFly() {
		return toFly;
	}
	public void setToFly(String toFly) {
		this.toFly = toFly;
	}
	public String getDepartureDate() {
		return departureDate;
	}
	public void setDepartureDate(String departureDate) {
		this.departureDate = departureDate;
	}
	public String getFlightNo() {
		return flightNo;
	}
	public void setFlightNo(String flightNo) {
		this.flightNo = flightNo;
	}
	public String getSeat() {
		return seat;
	}
	public void setSeat(String seat) {
		this.seat = seat;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getOrderState() {
		return orderState;
	}
	public void setOrderState(String orderState) {
		this.orderState = orderState;
	}
}
